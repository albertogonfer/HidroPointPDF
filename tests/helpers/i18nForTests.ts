export function createTestI18n(lng: 'es' | 'en' = 'es') {
  const instance = i18n.createInstance()
  instance.use(initReactI18next).init({
    lng,
    fallbackLng: 'en',
    resources: {
      es: { translation: es },
      en: { translation: en },
    },
    interpolation: { escapeValue: false },
  })
  return instance
}

